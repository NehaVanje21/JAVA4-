package Neha;

public class ArrayEx2 {
	
	public void display(int arr[]) {
		
	for(int i=0;i<arr.length;i++)
	{
		System.out.println(arr[i] + " ");
		
	}

}
	public static void main(String[] args) {
		ArrayEx2 a = new ArrayEx2();
		int arr[]= {1,2,3,4,5,6,7,8};
		a.display(arr);
	}
}
			
